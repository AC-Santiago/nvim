local api = vim.api
local getNeoTreeWidth = function()
    for _, win in pairs(api.nvim_tabpage_list_wins(0)) do
        if vim.bo[api.nvim_win_get_buf(win)].ft == "neo-tree" then
            return api.nvim_win_get_width(win)
        end
    end
    return 0
end

